"""Карта соответствия: id субъекта РФ в ФГИС (строка) → id федерального округа ФГИС (1..8).

Проверено кросс-чеком против живого `filters/regions` (85 субъектов, id 1..85 подряд).
ВАЖНО: id 1..79 совпадают со стандартными кодами субъектов, а «хвост» ФГИС нумерует
ПО-СВОЕМУ (не как автомобильные/ОКАТО коды): 80=Ненецкий АО, 81=ХМАО-Югра,
82=Чукотский АО, 83=ЯНАО, 84=Республика Крым, 85=г. Севастополь.
Ключи — строками с ведущим нулём для <10 ("01", "05", ...). Неизвестный id → 0.

Используется при синхронизации справочника регионов: ФГИС отдаёт id субъекта (int),
округ по нему не приходит — восстанавливаем по этой карте.
"""

# id федерального округа → список id субъектов ФГИС (строками, с ведущим нулём для <10)
_FED_CODES: dict[int, list[str]] = {
    1: ["31", "32", "33", "36", "37", "40", "44", "46", "48", "50",
        "57", "62", "67", "68", "69", "71", "76", "77"],  # ЦФО
    2: ["10", "11", "29", "35", "39", "47", "51", "53", "60", "78", "80"],  # СЗФО (80=Ненецкий АО)
    3: ["01", "08", "23", "30", "34", "61", "84", "85"],  # ЮФО (84=Крым, 85=Севастополь)
    4: ["05", "06", "07", "09", "15", "20", "26"],  # СКФО
    5: ["02", "12", "13", "16", "18", "21", "43", "52", "56",
        "58", "59", "63", "64", "73"],  # ПФО
    6: ["45", "66", "72", "74", "81", "83"],  # УФО (81=ХМАО-Югра, 83=ЯНАО)
    7: ["04", "17", "19", "22", "24", "38", "42", "54", "55", "70"],  # СФО
    8: ["03", "14", "25", "27", "28", "41", "49", "65", "75", "79", "82"],  # ДФО (82=Чукотский АО)
}

# Разворачиваем в плоскую карту: код субъекта → id округа.
REGION_FED: dict[str, int] = {
    code: fed for fed, codes in _FED_CODES.items() for code in codes
}


def _normalize(code: str) -> str:
    """Приводит код к каноничному виду карты: числовые <10 дополняем нулём ("1" → "01")."""
    key = (code or "").strip()
    if key.isdigit() and len(key) == 1:
        key = "0" + key
    return key


def region_fed(code: str) -> int:
    """id федерального округа (1..8) по коду субъекта. Неизвестный код → 0.

    Устойчив к коду без ведущего нуля (ФГИС может прислать id=1 для Адыгеи → "01").
    """
    return REGION_FED.get(_normalize(code), 0)
