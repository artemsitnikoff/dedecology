_MAX_LEN = 280
# Маркеры отказа/не-цитаты — если модель отказалась («Я не могу…») или ушла в прозу.
_REFUSAL_MARKERS = (
    "не могу", "не буду", "не стану", "извин", "ложн", "выдум", "придум",
    "as an ai", "i can't", "i cannot", "i'm unable", "sorry",
)


def _clean_quote(raw: str | None) -> str | None:
    """Валидирует ответ CLI: похоже на «цитата» — Автор, без отказа. Иначе None."""
    if not raw:
        return None
    s = " ".join(raw.split()).strip()
    if not s or len(s) > _MAX_LEN:
        return None
    low = s.lower()
    if any(m in low for m in _REFUSAL_MARKERS):
        return None
    # Должно выглядеть как цитата: кавычки-ёлочки + автор через тире.
    if "«" not in s or "»" not in s or "—" not in s:
        return None
    return s


async def nature_quote() -> str:
    """РАЗНАЯ подлинная цитата: claude приводит реальную → валидация → иначе фолбэк-список."""
    raw = await claude_cli_complete(
        prompt=_build_prompt(),
        model=settings.CLAUDE_QUOTE_MODEL,
        timeout=settings.CLAUDE_QUOTE_TIMEOUT,
    )
    cleaned = _clean_quote(raw)
    if cleaned:
        return cleaned
    logger.info("[quotes] CLI пусто/отказ/мусор — фолбэк на выверенный список")
    return random.choice(_QUOTES)
