"""Мотивирующая цитата о природе для успешного приёма обращения.

Генерируется через claude CLI (claude_cli_complete). При любом сбое CLI —
честный фолбэк на реальную цитату известного человека. Функция nature_quote
ВСЕГДА возвращает непустую строку и никогда не бросает исключений.
"""

import logging
import random

from ..config import settings
from .claude_cli import claude_cli_complete

logger = logging.getLogger(__name__)

_PROMPT = (
    "Придумай ОДНУ короткую мотивирующую цитату о природе или бережном "
    "отношении к ней, приписанную известному человеку (писатель, учёный, "
    "философ, поэт). Формат СТРОГО: «цитата» — Имя Фамилия. Только сама "
    "цитата с автором, без вступлений, кавычки-ёлочки обязательны."
)

# Реальные цитаты известных людей о природе (фолбэк на случай недоступности CLI).
_FALLBACK = [
    "«В каждой прогулке на природе человек получает гораздо больше, чем ищет.» — Джон Мьюир",
    "«Природа никогда не обманывает нас; обманываем себя всегда мы сами.» — Жан-Жак Руссо",
    "«Я ушёл в лес, потому что хотел жить осознанно.» — Генри Дэвид Торо",
    "«Если хочешь познать природу, дай ей говорить с тобою на её языке.» — Михаил Пришвин",
    "«Счастье — это быть с природой, видеть её, говорить с ней.» — Лев Толстой",
    "«Земля — это не наследство от родителей, а ссуда у наших детей.» — Антуан де Сент-Экзюпери",
    "«В природе ничто не возникает напрасно.» — Аристотель",
]

# Потолок длины цитаты в ответе (анти-разрастание при болтливости модели).
_MAX_LEN = 280


async def nature_quote() -> str:
    """Короткая цитата о природе. ВСЕГДА непустая строка (CLI → фолбэк)."""
    result = await claude_cli_complete(
        prompt=_PROMPT,
        model=settings.CLAUDE_QUOTE_MODEL,
        timeout=settings.CLAUDE_QUOTE_TIMEOUT,
    )
    if result:
        # Схлопываем в одну строку и подрезаем до потолка.
        cleaned = " ".join(result.split()).strip()
        if cleaned:
            return cleaned[:_MAX_LEN].strip()
        logger.warning("[quotes] CLI вернул пустое после очистки — фолбэк")
    return random.choice(_FALLBACK)
