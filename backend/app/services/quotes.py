"""Мотивирующая цитата о природе для успешного приёма обращения.

Генерируется через claude CLI (claude_cli_complete). При любом сбое CLI —
честный фолбэк на реальную цитату известного человека. Функция nature_quote
ВСЕГДА возвращает непустую строку и никогда не бросает исключений.
"""

import logging
import random

from ..config import settings
from .claude_cli import claude_cli_complete

logger = logging.getLogger(__name__)

# Рандомизируем промпт, чтобы цитаты НЕ повторялись: у `claude --print` нет флага
# температуры, поэтому разнообразие даём через случайную тему + категорию автора +
# nonce (ломает детерминизм и любое кэширование) и явный запрет на заезженные.
_THEMES = [
    "лесах и деревьях",
    "реках, морях и океанах",
    "горах и бескрайних просторах",
    "птицах и диких животных",
    "смене времён года",
    "бережном отношении к планете",
    "чистоте окружающего мира",
    "тишине и красоте дикой природы",
    "небе, ветре и воздухе",
    "земле, почве и урожае",
    "неразрывной связи человека и природы",
    "ответственности перед будущими поколениями",
]
_KINDS = [
    "писателю",
    "учёному",
    "философу",
    "поэту",
    "путешественнику",
    "натуралисту",
    "художнику",
    "общественному деятелю",
]


def _build_prompt() -> str:
    """Случайный промпт (тема + категория автора + nonce) → разные цитаты каждый раз."""
    theme = random.choice(_THEMES)
    kind = random.choice(_KINDS)
    nonce = random.randint(1000, 9999)
    return (
        f"Придумай ОДНУ короткую цитату о {theme}, приписанную известному {kind}. "
        "Каждый раз выбирай РАЗНОГО автора и свежую, небанальную мысль. НЕ используй "
        "заезженные цитаты (особенно Джона Мьюира про прогулку на природе). Формат "
        "СТРОГО: «цитата» — Имя Фамилия. Только сама цитата с автором, без вступлений, "
        f"кавычки-ёлочки обязательны. (вариант #{nonce})"
    )

# Реальные цитаты известных людей о природе (фолбэк на случай недоступности CLI).
_FALLBACK = [
    "«В каждой прогулке на природе человек получает гораздо больше, чем ищет.» — Джон Мьюир",
    "«Природа никогда не обманывает нас; обманываем себя всегда мы сами.» — Жан-Жак Руссо",
    "«Я ушёл в лес, потому что хотел жить осознанно.» — Генри Дэвид Торо",
    "«Если хочешь познать природу, дай ей говорить с тобою на её языке.» — Михаил Пришвин",
    "«Счастье — это быть с природой, видеть её, говорить с ней.» — Лев Толстой",
    "«Земля — это не наследство от родителей, а ссуда у наших детей.» — Антуан де Сент-Экзюпери",
    "«В природе ничто не возникает напрасно.» — Аристотель",
    "«Взгляни глубоко в природу, и тогда ты поймёшь всё гораздо лучше.» — Альберт Эйнштейн",
    "«Земля даёт достаточно, чтобы удовлетворить нужды каждого, но не жадность каждого.» — Махатма Ганди",
    "«Что пользы в доме, если у вас нет сносной планеты, чтобы его поставить?» — Генри Дэвид Торо",
    "«Леса предшествуют народам, а пустыни следуют за ними.» — Франсуа-Рене де Шатобриан",
    "«Тысячи людей могли прожить без любви, и ни один — без воды.» — Уистен Хью Оден",
    "«Мы не унаследовали Землю у предков — мы взяли её в долг у потомков.» — индейская мудрость",
]

# Потолок длины цитаты в ответе (анти-разрастание при болтливости модели).
_MAX_LEN = 280


async def nature_quote() -> str:
    """Короткая цитата о природе. ВСЕГДА непустая строка (CLI → фолбэк)."""
    result = await claude_cli_complete(
        prompt=_build_prompt(),
        model=settings.CLAUDE_QUOTE_MODEL,
        timeout=settings.CLAUDE_QUOTE_TIMEOUT,
    )
    if result:
        # Схлопываем в одну строку и подрезаем до потолка.
        cleaned = " ".join(result.split()).strip()
        if cleaned:
            return cleaned[:_MAX_LEN].strip()
        logger.warning("[quotes] CLI вернул пустое после очистки — фолбэк")
    return random.choice(_FALLBACK)
