"""Нормализация ТИПА субъекта РФ (региона) и пункта к единой канонической форме.

Проблема рассинхрона: DaData-Подсказки отдают ``region_with_type`` в СОКРАЩЁННОЙ
форме («Мурманская обл», «Респ Татарстан», «г Москва», «Ханты-Мансийский АО»), а
AI-разбор и сид-данные пишут ПОЛНУЮ («Мурманская область», «Республика Татарстан»,
«Москва», «… автономный округ»). Из-за этого фильтр/группировка по региону дробит
один субъект на два значения. Здесь — единая нормализация ТИПА субъекта к полной
форме.

Принципы:
- трогаем ТОЛЬКО слово-тип субъекта, ИМЯ субъекта и его регистр не меняем;
- совпадение типа — по словесным границам (``\\b`` / якоря ``^``/``$``),
  без учёта регистра самого слова-типа;
- лишние пробелы схлопываются;
- идемпотентность: повторный вызов ничего не меняет;
- пусто / ``None`` → ``""`` (никогда не бросаем исключений).
"""

import re

_WS_RE = re.compile(r"\s+")


def _collapse_ws(value: str) -> str:
    """Схлопывает любые пробельные последовательности в один пробел + strip."""
    return _WS_RE.sub(" ", value).strip()


# Ведущий тип «город» у городов федерального значения («г Москва» / «г. Москва» /
# «г.Москва»). Вынесен в константу — используется и в normalize_region, и в
# region_match_key (там же, где приводим обе стороны сопоставления к одному виду).
_LEADING_GOROD_RE = re.compile(r"^г\.\s*|^г\s+", re.IGNORECASE)


# Порядок ВАЖЕН:
#   1) ведущий тип «город» (фед. города) и «Респ» — якорь ^;
#   2) «Аобл» обрабатываем РАНЬШЕ «обл», иначе общий хвост «обл» не даст
#      «автономной области» отработать (хотя «\bАобл» и «\bобл» не пересекаются,
#      порядок фиксируем явно для читаемости);
#   3) «АО» — последним, когда «обл/Аобл» уже превратились в «область».
# Все типы сопоставляются без учёта регистра (re.IGNORECASE).
_REGION_SUBS: tuple[tuple[re.Pattern[str], str], ...] = (
    # «г Москва» / «г. Москва» / «г.Москва» / «г Санкт-Петербург» →
    # срезаем ведущий тип «город» у городов федерального значения.
    (_LEADING_GOROD_RE, ""),
    # «Респ Татарстан» / «Респ. Саха (Якутия)» / «Респ.Коми» → «Республика …».
    (re.compile(r"^Респ\.\s*|^Респ\s+", re.IGNORECASE), "Республика "),
    # «Еврейская Аобл» → «… автономная область» (раньше правила «обл»).
    (re.compile(r"\bАобл\.?\s*$", re.IGNORECASE), "автономная область"),
    # «Мурманская обл» / «Мурманская обл.» → «… область».
    (re.compile(r"\bобл\.?\s*$", re.IGNORECASE), "область"),
    # «Ханты-Мансийский АО» / «… а.о.» → «… автономный округ».
    (re.compile(r"\bА\.?О\.?\s*$", re.IGNORECASE), "автономный округ"),
)


def normalize_region(s: str) -> str:
    """Приводит ТИП субъекта РФ к полной канонической форме (имя не трогаем).

    Покрываемые преобразования:
      «Мурманская обл» / «Мурманская обл.»  → «Мурманская область»
      «Респ Татарстан»                      → «Республика Татарстан»
      «Еврейская Аобл»                      → «Еврейская автономная область»
      «Ханты-Мансийский АО» / «… а.о.»      → «Ханты-Мансийский автономный округ»
      «г Москва» / «г. Москва»              → «Москва» (срез ведущего «город»)
    Уже-полные формы, «… край», голое имя — без изменений. Пусто/None → "".
    Идемпотентно: ``normalize_region(normalize_region(x)) == normalize_region(x)``.
    """
    if not s:
        return ""
    out = _collapse_ws(str(s))
    if not out:
        return ""
    for pattern, repl in _REGION_SUBS:
        out = pattern.sub(repl, out)
    return _collapse_ws(out)


# Графические варианты тире/дефиса: ФГИС пишет ОБЫЧНЫЙ дефис («Ханты-Мансийский
# автономный округ - Югра»), а DaData/AI/оператор легко дадут длинное тире («… — Югра»)
# или минус. Для СОПОСТАВЛЕНИЯ все они эквивалентны.
_DASH_RE = re.compile("[‐‑‒–—―−]")
# Пробелы вокруг дефиса при сопоставлении не значимы: «область - Кузбасс» ≡ «область-Кузбасс».
_DASH_WS_RE = re.compile(r"\s*-\s*")


def region_match_key(s: str) -> str:
    """Ключ сопоставления имени субъекта РФ со справочником ``regions`` (синхр. из ФГИС).

    ЗАЧЕМ: в УТКО-выгрузке «Субъект РФ» обязан быть ИМЕНЕМ ИЗ СПРАВОЧНИКА (иначе УТКО
    отвергнет строку), а в инциденте лежит текст DaData/AI, который отличается от него
    косметикой: регистром («САНКТ-ПЕТЕРБУРГ»), «ё» vs «е», длинным тире вместо дефиса,
    пробелами вокруг дефиса, ведущим «г.» у городов федерального значения. Ключ
    гасит ровно эту косметику, чтобы «Санкт-Петербург» нашёл канон «г. Санкт-Петербург».
    Сам ключ — НЕ отображаемое значение: наружу всегда идёт ``Region.name`` из справочника.

    Преобразования: lower · «ё» → «е» · любое тире/минус → «-» · срез ведущего «г.»/«г » ·
    схлопывание пробелов вокруг дефиса · схлопывание пробелов + strip.
    Ключ достаточно различающий: на всех 85 субъектах ФГИС коллизий нет (см. тесты).
    Пусто / ``None`` → ``""`` (никогда не бросаем исключений).
    """
    if not s:
        return ""
    out = _collapse_ws(str(s)).lower().replace("ё", "е")
    if not out:
        return ""
    out = _DASH_RE.sub("-", out)
    out = _LEADING_GOROD_RE.sub("", out)
    out = _DASH_WS_RE.sub("-", out)
    return _collapse_ws(out)


# Полнословный «город X» → аббревиатура «г X» (DaData-стиль). ИНЫЕ типы пунктов
# (г / г. / пгт / с / д / рп / пос) НЕ трогаем — см. docstring normalize_city.
_CITY_GOROD_RE = re.compile(r"^город\s+", re.IGNORECASE)


def normalize_city(s: str) -> str:
    """Лёгкая нормализация пункта: схлопывание пробелов + «город X» → «г X».

    ОСТОРОЖНО — намеренно консервативна. Тип пункта НЕ унифицируется: формы
    «г Кинель» / «г. Кинель» / «Кинель» (DaData с типом vs AI/сид без типа vs с
    точкой) НЕОДНОЗНАЧНЫ — какая «каноническая», зависит от источника, а агрессивная
    правка рискует сломать другие типы пунктов (пгт/с/д/рп/пос). Поэтому здесь —
    только безопасное схлопывание пробелов и приведение ПОЛНОГО слова «город» к
    аббревиатуре «г» (это однозначно и совпадает со стилем DaData ``city_with_type``).
    Унификация наличия/точки префикса оставлена на уровень источника (DaData).

    Пусто/None → "". Идемпотентно.
    """
    if not s:
        return ""
    out = _collapse_ws(str(s))
    if not out:
        return ""
    out = _CITY_GOROD_RE.sub("г ", out)
    return _collapse_ws(out)
